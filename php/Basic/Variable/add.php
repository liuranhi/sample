<?php

$atk = 12408;
$hp = 14308; 

// 加算
$result = $atk + $hp;
print $result; // 26716
print(PHP_EOL);

// 引き算
$result = $atk - $hp; // -1900
print $result;
print(PHP_EOL);

// 掛け算
$result = $atk * $hp;
print $result; // 177533664
print(PHP_EOL);

// 割り算
$result = $atk / $hp;
print $result; // 0.86720715683534
print(PHP_EOL);

// 割り算の余り
$result = $atk % $hp;
print $result; // 12408
print(PHP_EOL);

?>
